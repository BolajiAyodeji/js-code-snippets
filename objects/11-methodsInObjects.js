// OOP
// Object Oriented Programming

const circle = {
  radius: 1,
  location: {
    x: 1,
    y: 1
  },
  isVisible: true,
  draw: function() {
    console.log('draw');
  }
}
circle.draw(); // Here the draw function is a Method in this Object

// If a function is part of an Object, in OOP it is called a Method
